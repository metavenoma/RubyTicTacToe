def print_board(board)
  grid_size = board.size

  # Print column headers
  print "\n\t\t\t    "
  (1..grid_size).each do |col|
    print " #{col}  "
  end
  puts

  # Print the board rows
  (0...grid_size).each do |row|
    # Print row separator for the top of each row
    if row == 0
      print "\t\t\t   "
      (0...grid_size).each do
        print " ---"
      end
      puts
    end

    # Print row label and row content
    print "\t\t\t  #{('A'.ord + row).chr}"
    (0...grid_size).each do |col|
      cell_value = board[row][col]
      print "| #{cell_value == 0 ? ' ' : (cell_value == 1 ? 'X' : 'O')} "
    end
    puts "|"

    # Print row separator for the bottom of each row
    print "\t\t\t   "
    (0...grid_size).each do
      print " ---"
    end
    puts
  end
end


